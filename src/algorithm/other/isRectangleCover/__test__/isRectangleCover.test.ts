import { isRectangleCover } from '../isRectangleCover';

test('isRectangleCover', () => {
  expect(isRectangleCover(
    [
      [
        1,
        1,
        3,
        3,
      ],
      [
        3,
        1,
        4,
        2,
      ],
      [
        3,
        2,
        4,
        4,
      ],
      [
        1,
        3,
        2,
        4,
      ],
      [
        2,
        3,
        3,
        4,
      ],
    ]
  ))
    .toBeTruthy();

  expect(isRectangleCover(
    [
      [
        1,
        1,
        2,
        3,
      ],
      [
        1,
        3,
        2,
        4,
      ],
      [
        3,
        1,
        4,
        2,
      ],
      [
        3,
        2,
        4,
        4,
      ],
    ]
  ))
    .toBeFalsy();

  expect(isRectangleCover(
    [
      [
        1,
        1,
        3,
        3,
      ],
      [
        3,
        1,
        4,
        2,
      ],
      [
        1,
        3,
        2,
        4,
      ],
      [
        3,
        2,
        4,
        4,
      ],
    ]
  ))
    .toBeFalsy();

  expect(isRectangleCover(
    [
      [
        1,
        1,
        3,
        3,
      ],
      [
        3,
        1,
        4,
        2,
      ],
      [
        1,
        3,
        2,
        4,
      ],
      [
        2,
        2,
        4,
        4,
      ],
    ]
  ))
    .toBeFalsy();
});
