import { pileBox } from '../pileBox';

test('pileBox', () => {
  expect(pileBox([
    [
      1,
      1,
      1,
    ],
    [
      2,
      2,
      2,
    ],
    [
      3,
      3,
      3,
    ],
  ]))
    .toBe(6);
  expect(pileBox([
    [
      1,
      1,
      1,
    ],
    [
      2,
      3,
      4,
    ],
    [
      2,
      6,
      7,
    ],
    [
      3,
      4,
      5,
    ],
  ]))
    .toBe(10);
});
